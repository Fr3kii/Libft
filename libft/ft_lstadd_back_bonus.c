#include "../inc/libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*tmp;

	if (!lst || !new)
		return ;
	tmp = ft_lstlast(*lst);
	if (tmp == 0)
	{
		*lst = new;
		return ;
	}
	(tmp)->next = new;
	return ;
}
