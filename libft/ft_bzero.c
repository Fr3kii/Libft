#include "../inc/libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t					i;
	unsigned char			*pt;

	i = 0;
	pt = s;
	while (n > i)
	{
		pt[i] = '\0';
		i++;
	}
}
