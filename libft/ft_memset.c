#include "../inc/libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char			*pt;

	pt = s;
	while (n > 0)
	{
		*pt = (unsigned char)c;
		pt++;
		n--;
	}
	return (s);
}
